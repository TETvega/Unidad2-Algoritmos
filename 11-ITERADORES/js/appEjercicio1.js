// Un Break lo que hace es detener un ciclo for, un while o un sitch
for (let i = 0; i < 10+1; i++) {
    
    if (i ===5) {
        console.log(`5 en letras "Cinco"`);
        break;
    } else {
        console.log(i);
    }
    
}

// Un continue lo que hace es dejar el codigo normal
